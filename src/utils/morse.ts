// Converts a message to morse code.
export function convertToMorseCode(message: string): string {
  message = message.toUpperCase();
  return message
    .split("\n")
    .map((line) => {
      return line
        .split(" ")
        .map((word) => {
          return word
            .split("")
            .map((char) => morseCode[char])
            .join("/");
        })
        .join(" ");
    })
    .join("\n");
}
